		public ContentType ContentType { get; }

		public ContentDisposition ContentDisposition { get; }

		public bool IsBinary { get; }

		public MimeType(string fileExtension, string contentType, bool isBinary)
		{
			FileExtension = fileExtension ?? throw new ArgumentNullException(nameof(fileExtension));

			if (contentType == null)
				throw new ArgumentNullException(nameof(contentType));
			ContentType = new ContentType(contentType);

			ContentDisposition = new ContentDisposition("inline");
			IsBinary = isBinary;
		}

